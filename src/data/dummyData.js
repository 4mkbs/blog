// Function to get related posts based on category and tags
export function getRelatedPosts(currentPostId, posts = dummyPosts) {
  const currentPost = posts.find((p) => p._id === currentPostId);
  if (!currentPost) return [];

  return posts
    .filter((p) => p._id !== currentPostId)
    .filter((p) => {
      // Match by category or tags
      const categoryMatch = p.category === currentPost.category;
      const tagMatch = currentPost.tags?.some((tag) => p.tags?.includes(tag));
      return categoryMatch || tagMatch;
    })
    .slice(0, 8);
}
